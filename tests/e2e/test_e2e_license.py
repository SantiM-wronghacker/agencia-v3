def _paid_until(days: int) -> str:
    dt = datetime.now(timezone.utc) + timedelta(days=days)
    return dt.date().isoformat()


async def _create_client_e2e(c, name="E2E Corp", pkg="basic", days=30) -> dict:
    res = await c.post(
        "/clients",
        json={"name": name, "package_type": pkg, "paid_until": _paid_until(days)},
        headers=ADMIN_HEADERS,
    )
    assert res.status_code == 200, f"Error creando cliente: {res.text}"
    return res.json()


async def _heartbeat_e2e(c, client_id: str, license_key: str) -> httpx.Response:
    return await c.post(
        "/heartbeat",
        json={
            "client_id": client_id,
            "license_key": license_key,
            "package_type": "basic",
            "timestamp": datetime.now(timezone.utc).isoformat(),
        },
    )


